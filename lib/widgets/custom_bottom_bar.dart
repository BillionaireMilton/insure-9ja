import 'package:flutter/material.dart';
import 'package:insure_9ja/core/app_export.dart';

class CustomBottomBar extends StatelessWidget {
  CustomBottomBar({this.onChanged});

  int selectedIndex = 0;

  List<BottomMenuModel> bottomMenuList = [
    BottomMenuModel(
      icon: ImageConstant.imgIconlybulkhome,
      type: BottomBarEnum.Iconlybulkhome,
    ),
    BottomMenuModel(
      icon: ImageConstant.imgIconlylightmessage,
      type: BottomBarEnum.Iconlylightmessage,
    ),
    BottomMenuModel(
      icon: ImageConstant.imgIconlylightcalendar,
      type: BottomBarEnum.Iconlylightcalendar,
    ),
    BottomMenuModel(
      icon: ImageConstant.imgIconlylightprofile,
      type: BottomBarEnum.Iconlylightprofile,
    )
  ];

  Function(BottomBarEnum)? onChanged;

  @override
  Widget build(BuildContext context) {
    return Container(
      decoration: BoxDecoration(
        color: ColorConstant.whiteA70001,
      ),
      child: BottomNavigationBar(
        backgroundColor: Colors.transparent,
        showSelectedLabels: false,
        showUnselectedLabels: false,
        elevation: 0,
        currentIndex: selectedIndex,
        type: BottomNavigationBarType.fixed,
        items: List.generate(bottomMenuList.length, (index) {
          return BottomNavigationBarItem(
            icon: CustomImageView(
              svgPath: bottomMenuList[index].icon,
              height: getSize(
                24,
              ),
              width: getSize(
                24,
              ),
              color: ColorConstant.gray50001,
            ),
            activeIcon: CustomImageView(
              svgPath: bottomMenuList[index].icon,
              height: getSize(
                24,
              ),
              width: getSize(
                24,
              ),
              color: ColorConstant.teal400,
            ),
            label: '',
          );
        }),
        onTap: (index) {
          selectedIndex = index;
          onChanged!(bottomMenuList[index].type);
        },
      ),
    );
  }
}

enum BottomBarEnum {
  Iconlybulkhome,
  Iconlylightmessage,
  Iconlylightcalendar,
  Iconlylightprofile,
}

class BottomMenuModel {
  BottomMenuModel({required this.icon, required this.type});

  String icon;

  BottomBarEnum type;
}

